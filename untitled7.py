    query_hist = extract_lbp_features(query_img)
    # Step 5: Compare with dataset images using Chi-square distance
    def chi2_distance(histA, histB, eps=1e-6):
        return 0.5 * np.sum(((histA - histB) ** 2) / (histA + histB + eps))
